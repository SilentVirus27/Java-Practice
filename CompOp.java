//wajp to demonstrate relational operator.
class CompOp {
    public static void main(String[] args) {
        int a=13,b=9;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        
    }
}
