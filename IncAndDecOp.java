//wajp to demonstrate increment operator and decrement
class IncAndDecOp {
    public static void main(String[] args) {
        int a=10 ,b=10;
        System.out.println("Before operation a:"+ a +" b: "+ b);
        int res1=++a,res2=--b;
        System.out.println("After operation a: "+ res1 +" b: "+ res2);
    }
}
